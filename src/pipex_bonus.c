#include "../include/pipex_bonus.h"

void	exe(t_tool *tool)
	{
	char	**gpath;
	char	*path;

	gpath = get_path(tool->envp);
	path = can_access(gpath, tool);
	free_split(gpath);
	if ((execve(path, tool->cmd, tool->envp)))
	{
		free_split(tool->cmd);
		free(tool->pid);
		free(path);
		reerror("execute failed", 1, 0);
	}
}

void	process(t_tool *tool)
{
	int	pipe_fd[2];

	if (pipe(pipe_fd) < 0)
		reerror("pipe failed", 0, 0);
	tool->pid[tool->i] = fork();
	if (tool->pid[tool->i] < 0)
		reerror("fork failed", 0, 0);
	if (tool->pid[tool->i] == 0)
	{
		tool->cmd = ft_split(tool->v[tool->i], ' ');
		close(pipe_fd[0]);
		dup2(pipe_fd[1], 1);
		close(pipe_fd[1]);
		exe(tool);
		free_split(tool->cmd);
	}
	else
	{
		close(pipe_fd[1]);
		dup2(pipe_fd[0], 0);
		close(pipe_fd[0]);
	}
	tool->i++;
}

void	file_init(t_tool *tool)
{
	int	file_fd;

	file_fd = open(tool->v[1], O_RDONLY);
	if (file_fd == -1)
		reerror("can't open file input", 0, 0);
	dup2(file_fd, 0);
	close(file_fd);
	tool->pid = malloc(sizeof(pid_t));
	tool->i = 2;
	tool->wp = 2;
}

void	ft_heredoc(t_tool *tool)
{
	int		file_fd;
	int		len;
	char	*str;

	len = ft_strlen(tool->v[2]);
	file_fd = open(".here_doc_temp", O_WRONLY | O_CREAT | O_TRUNC, 0644);
	str = get_next_line(0);
	while (str != NULL)
	{
		if (ft_strncmp(str, tool->v[2], len) == 0 && ft_strlen(str) - 1 == len)
		{
			free(str);
			close(file_fd);
			file_fd = open(".here_doc_temp", O_RDONLY);
			dup2(file_fd, 0);
			close(file_fd);
			tool->pid = malloc(sizeof(pid_t));
			tool->i = 3;
			tool->wp = 3;
			return ;
		}
		write(file_fd, str, ft_strlen(str));
		free(str);
		str = get_next_line(0);
	}
}

int	main(int c, char **v, char **envp)
{
	t_tool	tool;

	if (c < 5 || (c < 6 && ft_strncmp(v[1], "here_doc", 8) == 0))
		return (pnf("failed input"));
	tool.c = c;
	tool.v = v;
	tool.all_cmd = &v[2];
	tool.envp = envp;
	if (ft_strncmp(v[1], "here_doc", 8) == 0)
	{
		ft_heredoc(&tool);
		unlink(".here_doc_temp");
	}
	else
		file_init(&tool);
	while (tool.i < c - 2)
		process(&tool);
	if (tool.pid[c - 3] != 0)
		last_cmd(&tool);
	while (tool.wp < c - 1)
		waitpid(tool.pid[tool.wp++], 0, 0);
	free(tool.pid);
}

